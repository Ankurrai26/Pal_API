package pojoClassDeserialization;

public class ErrorList {

	
	private String exceptionCd;
	private String exceptionCatgCd;
	private String errDescription;
	private String errActualMessage;
	private String errSourceCd;
	private String errProcessStatusCd;
	private String errField;
	private String errRecord;
	public String getExceptionCd() {
		return exceptionCd;
	}
	public void setExceptionCd(String exceptionCd) {
		this.exceptionCd = exceptionCd;
	}
	public String getExceptionCatgCd() {
		return exceptionCatgCd;
	}
	public void setExceptionCatgCd(String exceptionCatgCd) {
		this.exceptionCatgCd = exceptionCatgCd;
	}
	public String getErrDescription() {
		return errDescription;
	}
	public void setErrDescription(String errDescription) {
		this.errDescription = errDescription;
	}
	public String getErrActualMessage() {
		return errActualMessage;
	}
	public void setErrActualMessage(String errActualMessage) {
		this.errActualMessage = errActualMessage;
	}
	public String getErrSourceCd() {
		return errSourceCd;
	}
	public void setErrSourceCd(String errSourceCd) {
		this.errSourceCd = errSourceCd;
	}
	public String getErrProcessStatusCd() {
		return errProcessStatusCd;
	}
	public void setErrProcessStatusCd(String errProcessStatusCd) {
		this.errProcessStatusCd = errProcessStatusCd;
	}
	public String getErrField() {
		return errField;
	}
	public void setErrField(String errField) {
		this.errField = errField;
	}
	public String getErrRecord() {
		return errRecord;
	}
	public void setErrRecord(String errRecord) {
		this.errRecord = errRecord;
	}
	
	
	
}
