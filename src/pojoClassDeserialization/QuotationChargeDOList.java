package pojoClassDeserialization;

public class QuotationChargeDOList {

	private String chargeClassCd;
	private int chargeAmount;
	private String chargePercentage;
	private String chargeType;
	
	public String getChargeClassCd() {
		return chargeClassCd;
	}
	public void setChargeClassCd(String chargeClassCd) {
		this.chargeClassCd = chargeClassCd;
	}
	public int getChargeAmount() {
		return chargeAmount;
	}
	public void setChargeAmount(int chargeAmount) {
		this.chargeAmount = chargeAmount;
	}
	public String getChargePercentage() {
		return chargePercentage;
	}
	public void setChargePercentage(String chargePercentage) {
		this.chargePercentage = chargePercentage;
	}
	public String getChargeType() {
		return chargeType;
	}
	public void setChargeType(String chargeType) {
		this.chargeType = chargeType;
	}
	
	
	
	
}
